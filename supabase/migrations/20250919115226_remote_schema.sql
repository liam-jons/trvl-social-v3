CREATE OR REPLACE FUNCTION public.update_vote_counts()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
  IF TG_OP = 'INSERT' THEN
    IF NEW.thread_id IS NOT NULL THEN
      IF NEW.vote_type = 'upvote' THEN
        UPDATE vendor_forum_threads SET upvotes = upvotes + 1 WHERE id = NEW.thread_id;
      ELSE
        UPDATE vendor_forum_threads SET downvotes = downvotes + 1 WHERE id = NEW.thread_id;
      END IF;
    ELSIF NEW.reply_id IS NOT NULL THEN
      IF NEW.vote_type = 'upvote' THEN
        UPDATE vendor_forum_replies SET upvotes = upvotes + 1 WHERE id = NEW.reply_id;
      ELSE
        UPDATE vendor_forum_replies SET downvotes = downvotes + 1 WHERE id = NEW.reply_id;
      END IF;
    END IF;
    RETURN NEW;
  ELSIF TG_OP = 'DELETE' THEN
    IF OLD.thread_id IS NOT NULL THEN
      IF OLD.vote_type = 'upvote' THEN
        UPDATE vendor_forum_threads SET upvotes = GREATEST(upvotes - 1, 0) WHERE id = OLD.thread_id;
      ELSE
        UPDATE vendor_forum_threads SET downvotes = GREATEST(downvotes - 1, 0) WHERE id = OLD.thread_id;
      END IF;
    ELSIF OLD.reply_id IS NOT NULL THEN
      IF OLD.vote_type = 'upvote' THEN
        UPDATE vendor_forum_replies SET upvotes = GREATEST(upvotes - 1, 0) WHERE id = OLD.reply_id;
      ELSE
        UPDATE vendor_forum_replies SET downvotes = GREATEST(downvotes - 1, 0) WHERE id = OLD.reply_id;
      END IF;
    END IF;
    RETURN OLD;
  END IF;
  RETURN NULL;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.warm_compatibility_cache(p_user_ids uuid[], p_group_id uuid DEFAULT NULL::uuid)
 RETURNS TABLE(users_processed integer, cache_hits integer, new_calculations integer)
 LANGUAGE plpgsql
AS $function$
DECLARE
  v_users_processed INTEGER := 0;
  v_cache_hits INTEGER := 0;
  v_new_calculations INTEGER := 0;
  v_user_id UUID;
  v_score INTEGER;
BEGIN
  -- Process all users
  FOREACH v_user_id IN ARRAY p_user_ids LOOP
    v_users_processed := v_users_processed + 1;

    -- Check if score exists in cache
    SELECT compatibility_score INTO v_score
    FROM get_compatibility_score_fast(v_user_id, p_group_id, 48);

    IF FOUND THEN
      v_cache_hits := v_cache_hits + 1;
    ELSE
      -- In a real implementation, this would trigger calculation
      v_new_calculations := v_new_calculations + 1;
    END IF;
  END LOOP;

  RETURN QUERY SELECT v_users_processed, v_cache_hits, v_new_calculations;
END;
$function$
;


