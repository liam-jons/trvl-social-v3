      last_reply_vendor_id = (
        SELECT vendor_id FROM vendor_forum_replies
        WHERE thread_id = OLD.thread_id
        ORDER BY created_at DESC
        LIMIT 1
      )
    WHERE id = OLD.thread_id;
    RETURN OLD;
  END IF;
  RETURN NULL;
