CREATE OR REPLACE FUNCTION public.get_compatibility_score_fast(p_user_id uuid, p_group_id uuid DEFAULT NULL::uuid, p_max_age_hours integer DEFAULT 24)
 RETURNS TABLE(compatibility_score integer, calculated_at timestamp with time zone, from_cache boolean, confidence numeric)
 LANGUAGE plpgsql
AS $function$
DECLARE
  v_cutoff_time TIMESTAMP WITH TIME ZONE := NOW() - (p_max_age_hours || ' hours')::INTERVAL;
BEGIN
  -- Try hot cache first (materialized view)
  IF p_group_id IS NULL THEN
    RETURN QUERY
    SELECT
      mv.avg_compatibility,
      mv.last_calculated,
      true as from_cache,
      CASE
        WHEN mv.calculation_count >= 5 THEN 0.90
        WHEN mv.calculation_count >= 3 THEN 0.80
        ELSE 0.70
      END::DECIMAL(3,2) as confidence
    FROM mv_hot_compatibility_scores mv
    WHERE mv.user_id = p_user_id
      AND mv.group_id IS NULL
      AND mv.last_calculated > v_cutoff_time
    LIMIT 1;

    IF FOUND THEN
      RETURN;
    END IF;
  END IF;

  -- Try regular cache
  RETURN QUERY
  SELECT
    gcs.compatibility_score,
    gcs.calculated_at,
    true as from_cache,
    0.85::DECIMAL(3,2) as confidence
  FROM group_compatibility_scores gcs
  WHERE gcs.user_id = p_user_id
    AND (p_group_id IS NULL OR gcs.group_id = p_group_id)
    AND gcs.calculated_at > v_cutoff_time
  ORDER BY gcs.calculated_at DESC
  LIMIT 1;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.get_connection_recommendations(p_user_id uuid, p_limit integer DEFAULT 10)
 RETURNS TABLE(user_id uuid, full_name text, avatar_url text, location text, shared_adventures integer, recommendation_score integer)
 LANGUAGE plpgsql
AS $function$
BEGIN
  RETURN QUERY
  WITH shared_adventures AS (
    -- Find users who were in the same groups
    SELECT
      gm2.user_id,
      COUNT(DISTINCT gm1.group_id) as adventure_count
    FROM group_members gm1
    JOIN group_members gm2 ON gm1.group_id = gm2.group_id
    JOIN groups g ON gm1.group_id = g.id
    WHERE gm1.user_id = p_user_id
      AND gm2.user_id != p_user_id
      AND g.status IN ('completed', 'active')
      -- Exclude existing connections
      AND NOT EXISTS (
        SELECT 1 FROM community_connections cc
        WHERE cc.user_id = p_user_id
          AND cc.connected_user_id = gm2.user_id
          AND cc.status IN ('accepted', 'blocked')
      )
      -- Exclude pending requests
      AND NOT EXISTS (
        SELECT 1 FROM connection_requests cr
        WHERE ((cr.requester_id = p_user_id AND cr.recipient_id = gm2.user_id)
            OR (cr.requester_id = gm2.user_id AND cr.recipient_id = p_user_id))
          AND cr.status = 'pending'
      )
    GROUP BY gm2.user_id
  )
  SELECT
    p.id as user_id,
    p.full_name,
    p.avatar_url,
    p.location,
    sa.adventure_count as shared_adventures,
    -- Simple recommendation score based on shared adventures
    (sa.adventure_count * 20)::INTEGER as recommendation_score
  FROM shared_adventures sa
  JOIN profiles p ON sa.user_id = p.id
  ORDER BY sa.adventure_count DESC, p.full_name ASC
  LIMIT p_limit;
END;
$function$
;

