CREATE OR REPLACE FUNCTION public.update_user_engagement_score(p_user_id uuid)
 RETURNS void
 LANGUAGE plpgsql
AS $function$
DECLARE
  total_posts INTEGER;
  total_comments INTEGER;
  total_reactions INTEGER;
  total_connections INTEGER;
  calculated_score DECIMAL(10,2);
BEGIN
  -- Count user's posts
  SELECT COUNT(*) INTO total_posts
  FROM community_posts
  WHERE user_id = p_user_id;

  -- Count user's comments
  SELECT COUNT(*) INTO total_comments
  FROM post_comments
  WHERE user_id = p_user_id;

  -- Count user's reactions
  SELECT COUNT(*) INTO total_reactions
  FROM post_reactions
  WHERE user_id = p_user_id;

  -- Count user's connections
  SELECT COUNT(*) INTO total_connections
  FROM community_connections
  WHERE user_id = p_user_id AND status = 'accepted';

  -- Calculate engagement score
  calculated_score := (total_posts * 10) +
                     (total_comments * 5) +
                     (total_reactions * 2) +
                     (total_connections * 15);

  -- Upsert engagement score
  INSERT INTO engagement_scores (
    user_id,
    total_posts,
    total_comments,
    total_reactions,
    total_connections,
    engagement_score,
    last_calculated,
    updated_at
  ) VALUES (
    p_user_id,
    total_posts,
    total_comments,
    total_reactions,
    total_connections,
    calculated_score,
    NOW(),
    NOW()
  )
  ON CONFLICT (user_id) DO UPDATE SET
    total_posts = EXCLUDED.total_posts,
    total_comments = EXCLUDED.total_comments,
    total_reactions = EXCLUDED.total_reactions,
    total_connections = EXCLUDED.total_connections,
    engagement_score = EXCLUDED.engagement_score,
    last_calculated = EXCLUDED.last_calculated,
    updated_at = EXCLUDED.updated_at;
END;
$function$
;

